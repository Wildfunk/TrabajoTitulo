        DefaultTableModel mod =(DefaultTableModel)jTable1.getModel();
        
        String cod= mod.getValueAt(ind, 0).toString();
        String nom= mod.getValueAt(ind, 1).toString();
        String rut= mod.getValueAt(ind, 2).toString();
        String asu= mod.getValueAt(ind, 3).toString();
        String est= mod.getValueAt(ind, 4).toString();
        String det= mod.getValueAt(ind, 5).toString();
        Solicitud nuevaSoli = new Solicitud(this,cod,nom,rut,asu,est,det);
        nuevaSoli.setVisible(true);
        this.setEnabled(false);
