    public void mostrarTabla(){
        try{
            stm=conex.createStatement();
            jTable1.removeAll();
            DateFormat dateFormat1 = new SimpleDateFormat("yyyy-MM-dd");
            Date date = new Date();
            ResultSet rs = stm.executeQuery("select cod_soli,nombre,rut_soli,asunto,detalle,estado from solicitud_prestamo"); //hacer select de tabla solicitudes

            DefaultTableModel mod =(DefaultTableModel)jTable1.getModel();
            mod.setRowCount(0);
            
            while(rs.next()){
                Object data[]={rs.getString("cod_soli"),rs.getString("nombre"),rs.getString("rut_soli"),rs.getString("asunto"),rs.getString("estado"),rs.getString("detalle")};
                //Object data[]={rs.getString("rut"),rs.getShort("Empleo_cod_empleo"),rs.getString("nombre"),rs.getString("tipo"),rs.getString("fono"),rs.getString("email"),rs.getString("hora_entrada"),rs.getString("hora_salida")};

                mod.addRow(data);
            }
            jTable1.getColumnModel().getColumn(5).setMaxWidth(0);

            jTable1.getColumnModel().getColumn(5).setMinWidth(0);

            jTable1.getColumnModel().getColumn(5).setPreferredWidth(0);
            //JOptionPane.showMessageDialog(null, "selec"+dateFormat1.format(date),"conexion",3);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "selec"+ex,"conexion",3);
        }
    }
