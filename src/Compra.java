    private void cmdAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAñadirActionPerformed
        String f= lblFechaD.getText();
        String np = txtProv.getText();
        String ni = cmbItem.getSelectedItem().toString();
        String ci = "";
        String can= txtCant.getText();
        String pu = txtPU.getText();
        
        int total;
        total = Integer.parseInt(can) * Integer.parseInt(pu);
        try{    
            stm=conex.createStatement();
            ResultSet rs = stm.executeQuery("select cod_item from item where nombre='"+ ni +"'");
            rs.next();
            ci= rs.getString(1);
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex,"conexion",3);
        }
        try{
            stm=conex.createStatement();
            stm.executeUpdate("insert into adquisicion_item values('"+f+"','"+np+"',"+ci+","+can+","+pu+","+total+")");
            stm.executeUpdate("update item set cantidad_real=cantidad_real + "+can+" where item.cod_item="+ci+"  ");
            JOptionPane.showMessageDialog(null,"Añadido Correctamente\nLa cantidad real del item se ha actualizado","conexion",3);
            mostrarTablaComp();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex,"conexion",3);
        }
    }//GEN-LAST:event_cmdAñadirActionPerformed

    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed
        int index = jtCompras.getSelectedRow();
        if (index<0){
            JOptionPane.showMessageDialog(null, "Seleccione una Comrpa","conexion",3);
        }else{
            switch (JOptionPane.showConfirmDialog(null, "Seguro desea ELIMINAR esta Compra?","confirmacion",2)){
                case 0:
                    DefaultTableModel mod =(DefaultTableModel)jtCompras.getModel();
                    String f = mod.getValueAt(index, 0).toString();
                    String np = mod.getValueAt(index, 1).toString();
                    String cod = mod.getValueAt(index, 2).toString();
                    try{    
                        stm=conex.createStatement();
                        ResultSet rs = stm.executeQuery("select cod_item from item where nombre='"+ cod +"'");
                        rs.next();
                        cod= rs.getString(1);

                    }catch(SQLException ex){
                        JOptionPane.showMessageDialog(null,ex,"conexion",3);
                    }
                    String sql= "delete from adquisicion_item where fecha='"+f+"' and nom_prov='"+np+"' and Item_cod_item="+cod;
                    System.out.println(sql);

                    try{
                        stm=conex.createStatement();
                        stm.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "Eliminado Correctamente","conexion",3);
                        jtCompras.clearSelection();
                        mostrarTablaComp();
                    }catch(SQLException ex){
                        JOptionPane.showMessageDialog(null, ex,"conexion",3);
                    }
                default:
                    break;
            }
            
        }
    }//GEN-LAST:event_cmdEliminarActionPerformed

