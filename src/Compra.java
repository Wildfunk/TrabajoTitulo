    public void mostrarTablaComp(){
        try{
            stm=conex.createStatement();
            jtCompras.removeAll();
            ResultSet rs = stm.executeQuery("select adquisicion_item.fecha , adquisicion_item.nom_prov , item.nombre , adquisicion_item.cantidad, adquisicion_item.precio_unit, adquisicion_item.precio_total FROM adquisicion_item INNER JOIN item ON adquisicion_item.Item_cod_item = item.cod_item");
            
            DefaultTableModel mod =(DefaultTableModel)jtCompras.getModel();
            mod.setRowCount(0);
            while(rs.next()){
                Object data[]={rs.getString("fecha"),rs.getString("nom_prov"),rs.getString("nombre"),rs.getString("cantidad"),rs.getString("precio_unit"),rs.getString("precio_total")};
                mod.addRow(data);
                
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex,"conexion",3);
        }
    }
    
    public void llenaCombo(){
        try{    
            cmbItem.removeAllItems();
            stm=conex.createStatement();
            ResultSet lista = stm.executeQuery("select nombre from item");

            while (lista.next()){
                cmbItem.addItem(lista.getString(1));
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex,"conexion",3);
        }
    }

